#include <iostream>
using namespace std;

/*
    Operadores lógicos
    - AND (&&)
    - OR (||)
    - NOT (!)
*/

int main() {
    int a = 10, b = 5;
    // Operadores aritméticos
    cout << a + b << endl; // Suma
    
    // Operadores relacionales
    cout << (a > b) << endl; // Mayor que
    cout << (a > 5 && b < 10) << endl;
}